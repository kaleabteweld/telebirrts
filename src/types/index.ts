/**
* @param appid Indicates the appID provided from telebirr
* @param appkey Indicates the appKey provided by telebirr platform
**/
export interface Iclient {
    appid: string;
    appkey: string;
}


/**
 * @param notifyUrl Indicates the end point URL from third party which will be used by telebirr platform to respond the Payment result
 * @param timeoutExpress Indicates the payment Order request timeout from third party which indicates the time for payment process to be ended. After this time the payment will not be processed and third party system can reinitiate again,the parameter value unit is Minutes
 * @param timestamp Indicates the time stamp on the request message. The timestamp should be in milliseconds
 */
export interface IRequestReq {
    notifyUrl: string;

    timeoutExpress: string;
    timestamp: string;
}

/**
 * @param receiveName Indicates the name of the person/organization who will receive the payment
 * @param shortCode Indicates third party  Short Code provided from telebirr
 * @param totalAmount Indicates the order amount in ETB. The value ranges from 0.01 to 100000000.00 and is accurate to two decimal places, The value will be authenticated by telebirr platform depending on the limit rule assigned to the customer
 **/
export interface IReceiver {
    receiveName: string;
    shortCode: string;
    totalAmount: string;
}

/**
 * @param outTradeNo Indicates the third party payment transaction order number that will be generated by third party system and it shall be unique
 * @param nonce Indicates a unique random string generated by third party system
 * @param subject Indicates the item or any other name for the payment that is being issued by the customer
**/
export interface ITransaction {
    outTradeNo: string;
    nonce: string;
    subject: string;
}

/**
 *  @param code Indicates the response code from telebirr platform
 *  @param msg Indicates the response message from telebirr platform
**/

export interface IResponse {
    code: string;
    message: string;
}

/**
 *  @param code Indicates the response code from telebirr platform
 *  @param msg Indicates the response message from telebirr platform
 *  @param outTradeNo Indicates the third party payment request order number stated on the payment request. 
 *  @param tradeNo Indicates the transaction  number in telebirr payment system
**/
export interface INotificationIRespons extends IResponse {
    data: {
        outTradeNo: string;
        tradeNo: string;
    }
}

/**
*  @param code Indicates the response code from telebirr platform
 *  @param msg Indicates the response message from telebirr platform
 *  @param data Indicates the response data from telebirr platform
 *  @param toPayUrl Indicates the payment URL from telebirr platform
 */
export interface IH5webResponse extends IResponse {
    data: {
        toPayUrl: string
    }
}

/**
*  @param code Indicates the response code from telebirr platform
 *  @param msg Indicates the response message from telebirr platform
 *  @param data Indicates the response data from telebirr platform
 *  @param toPayMsg Msg for redirecting to  telebirr App Payment for android use IAndroidAppResponse for ios its just a string url
 */
export interface IMobileAppResponse extends IResponse {
    data: {
        toPayMsg: string
    }
}

/**
 *  @param PackageName cn.tydic.ethiopay
 *  @param Activity cn.tydic.ethiopay.PayForOtherAppActivity
**/
export interface IAndroidAppResponse {
    PackageName: string;
    Activity: string;
}